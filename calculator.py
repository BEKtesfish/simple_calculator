class calculator:
    def add(self,num1,num2):
        num1_num2=num1 + num2
        return f"{num1} + {num2} = {num1_num2}"
    def subtract(self,num1,num2):
        num1_num2=num1-num2
        return f"{num1} - {num2} = {num1_num2}"
    def multiply(self,num1,num2):
        num1_num2=num1 * num2
        return f"{num1} * {num2} = {num1_num2}"
    
